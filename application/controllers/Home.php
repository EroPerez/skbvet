class Home extends CI_Controller
{
    /*
    |--------------------------------------------------------------------------
    | Home Controller
    |--------------------------------------------------------------------------
    |
    | This controller handles Dashboard.
    |
    */
    public function __construct()
    {
        parent::__construct();
        $this->load->library('auth');
        $this->auth->route_access();
    }

    /**
     * Display a Dashboard.
     *
     * @return mixed
     */
    public function index()
    {

        return $this->load->view('home');
    }
}