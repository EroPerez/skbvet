<?php

class M_levels extends CI_Model {

///////////////////////////////////////////////////////////////////////////////////// 
//Tabla tblCountries Atributos //////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////

    var $idlevels = '';
    var $rolename = '';

/////////////////////////////////////////////////////////////////////////////////////     
// M�todos de la clase tblCountries .
/////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////
//Construtor de la clase.

    public function __construct() {
        parent::__construct();
        $this->load->database();
    }

/////////////////////////////////////////////////////////////////////////////////////
//////////////////////////////////////////////////////////////////////////////////////////   
/// Esta funci�n inserta un elemento en la tabla.

    public function set_levels($data) {
        $this->idlevels = $data['d_idlevels'];
        $this->rolename = $data['d_rolename '];

        $resultado = $this->db->insert('levels', $this);
    }

/////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////// 
// Esta funci�n actualiza un elemento en la tabla.

    function update_levels($data) {
        $this->db->set('rolename', $data['d_rolename']);

        $this->db->where('idlevels', $data['d_idlevels']);

        $this->db->update('levels');
    }

/////////////////////////////////////////////////////////////////////////////////////
//////////////////////////////////////////////////////////////////////////////////////////   
// Esta funci�n elimina un elemento en la tabla.

    public function del_levels($idlevels) {
        $this->db->delete('levels', array('idlevels' => $idlevels));
    }

/////////////////////////////////////////////////////////////////////////////////////
//////////////////////////////////////////////////////////////////////////////////////////   
// Esta funci�n busca elementos en la tabla por nombre

    public function find_levels($data) {
        $this->db->select('levels.*');
        $this->db->from('levels');
        $this->db->like('rolename', $data['d_rolename']);

        $query = $this->db->get();
        return $query->result_array();
    }

/////////////////////////////////////////////////////////////////////////////////////
/// Retorna todos los datos de un elemento solicitado.

    public function get_one_levels($idlevels) {
        $query = $this->db->get_where('levels', array('idlevels' => $idlevels));
        return $query->result_array();
    }

/////////////////////////////////////////////////////////////////////////////////////
/// Retorna todos los datos de todos los elementos.

    public function get_all_levels() {
        $query = $this->db->get('levels');
        return $query->result_array();
    }

}

