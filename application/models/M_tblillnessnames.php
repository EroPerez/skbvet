<?php

class M_tblillnessnames extends CI_Model {

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////// 
//Tabla tblillnessnames Atributos 
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

    var $recn = '';
    var $name = '';
    var $code = '';

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////     
// M�todos de la clase tblillnessnames.
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//Construtor de la clase.
    public function __construct() {
        parent::__construct();
        $this->load->database();
    }

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////   
// Esta funci�n inserta un elemento en la tabla.
    public function set_illnessname($data) {
        $this->name = $data['d_name'];
        $this->code = $data['d_code'];
        $query = $this->db->insert('tblillnessnames', $this);
        return $query;
    }

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////// 
// Esta funci�n actualiza un elemento en la tabla.    
    function update_illnessname($data) {
        $this->recn = $data['d_recn'];
        $this->name = $data['d_name'];
        $this->code = $data['d_code'];
        $this->db->where('recn', $data['d_recn']);
        $query = $this->db->update('tblillnessnames', $this);
        return $query;
    }

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////  
// Esta funci�n elimina un elemento en la tabla.
    public function del_illnessname($recn) {
        $query = $this->db->delete('tblillnessnames', array('recn' => $recn));
        return $query;
    }

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////   
// Esta funci�n busca elementos en la tabla por nombre
    public function find_illnessname($data) {
        $this->db->select('tblillnessnames.*');
        $this->db->from('tblillnessnames');
        $this->db->where('recn', $data['d_recnactivo']);
        $query = $this->db->get();
        return $query->result_array();
    }

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// Retorna todos los datos de un elemento solicitado.
    public function get_one_illnessname($recn) {
        $this->db->select('tblillnessnames.*');
        $this->db->from('tblillnessnames');
        $this->db->where('recn', $recn);
        $query = $this->db->get();
        return $query->result_array();
    }

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// Retorna todos los datos de todos los elementos.
    public function get_all_illnessname() {
        $query = $this->db->get('tblillnessnames');
        return $query->result_array();
    }

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
}

