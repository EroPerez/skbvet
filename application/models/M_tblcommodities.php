<?php

class M_tblcommodities extends CI_Model {

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//Tabla tblCommodities Atributos 
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

    var $recn = '';
    var $name = '';

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////     
// M�todos de la clase tblCommodities.
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//Construtor de la clase.
    public function __construct() {
        parent::__construct();
        $this->load->database();
    }

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////// 
//Esta funci�n inserta un elemento en la tabla.
    public function set_Commodities($data) {
        $this->name = $data['d_name'];
        $query = $this->db->insert('tblcommodities', $this);
        return $query;
    }

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////// 
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////  
// Esta funci�n actualiza un elemento en la tabla . Modificada Leo 17/02  
    function update_Commodities($data) {
        $this->db->set('name', $data['d_name']);
        $this->db->where('recn', $data['d_recn']);
        $query = $this->db->update('tblcommodities');
        return $query;
    }

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////// 
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////// 
// Esta funci�n elimina un elemento en la tabla.
    public function del_Commodities($recn) {
        $query = $this->db->delete('tblcommodities', array('recn' => $recn));
        return $query;
    }

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////// 
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////   
// Esta funci�n busca elementos en la tabla por nombre
    public function find_Commodities($data) {
        $this->db->select('tblcommodities.*');
        $this->db->from('tblcommodities');
        $this->db->like('name', $data['d_name']);
        $query = $this->db->get();
        return $query->result_array();
    }

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////// 
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////// 
/// Retorna todos los datos de un elemento solicitado.
    public function get_one_Commodities($recn) {
        $query = $this->db->get_where('tblcommodities', array('recn' => $recn));
        return $query->result_array();
    }

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////// 
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////// 
/// Retorna todos los datos de todos los elementos.
    public function get_all_Commodities() {
        $query = $this->db->get('tblcommodities');
        return $query->result_array();
    }

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////// 
}

