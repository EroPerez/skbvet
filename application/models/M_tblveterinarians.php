    public function find_Veterinarians($data) {
        $this->db->select('tblveterinarians.*');
        $this->db->from('tblveterinarians');
        $this->db->like('name', $data['d_name']);
        $query = $this->db->get();
        return $query->result_array();
    }

//////////////////////////////////////////////////////////////////////////////////////////////////////////////
//////////////////////////////////////////////////////////////////////////////////////////////////////////////
