<?php

class M_tbltraders extends CI_Model {

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////// 
//Tabla tblCountries Atributos.
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

    var $recn = '';
    var $name = '';
    var $type = '';

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////     
// M�todos de la clase tblCountries .
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//Construtor de la clase.

    public function __construct() {
        parent::__construct();
        $this->load->database();
    }

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////   
/// Esta funci�n inserta un elemento en la tabla.

    public function set_Traders($data) {
        $this->name = $data['d_name'];
        $this->type = $data['d_type'];
        $query = $this->db->insert('tbltraders', $this);
        return $query;
    }

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////// 
// Esta funci�n actualiza un elemento en la tabla.

    function update_Traders($data) {
        $this->recn = $data['d_recn'];
        $this->name = $data['d_name'];
        $this->type = $data['d_type'];
        $this->db->where('recn', $data['d_recn']);
        $query = $this->db->update('tbltraders', $this);
        return $query;
    }

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////   
// Esta funci�n elimina un elemento en la tabla.

    public function del_Traders($recn) {
        $query = $this->db->delete('tbltraders', array('recn' => $recn));
        return $query;
    }

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////   
// Esta funci�n busca elementos en la tabla por nombre

    public function find_Traders($data) {
        $this->db->select('tbltraders .*');
        $this->db->from('tbltraders ');
        $this->db->like('name', $data['d_name']);
        $query = $this->db->get();
        return $query->result_array();
    }

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/// Retorna todos los datos de un elemento solicitado.

    public function get_one_Traders($recn) {
        $this->db->select('tbltraders.*');
        $this->db->from('tbltraders');
        $this->db->where('recn', $recn);
        $query = $this->db->get();
        return $query->result_array();
    }

/////////////////////////////////////////////////////////////////////////////////////
/// Retorna todos los datos de todos los elementos.

    public function get_all_Traders() {
        $query = $this->db->get('tbltraders');
        return $query->result_array();
    }

/////////////////////////////////////////////////////////////////////////////////////
/// Retorna  datos de traders por tipo.

    public function get_Traders_by_type($type) {
        $this->db->select('tbltraders.*');
        $this->db->from('tbltraders');
        $this->db->where('type', $type);
        $query = $this->db->get();
        return $query->result_array();
    }

}

