<?php

class M_tbltestnames extends CI_Model {

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////// 
//Tabla tbltestnames Atributos 
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

    var $recn = '';
    var $name = '';

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////     
// M�todos de la clase tblillnessnames.
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//Construtor de la clase.
    public function __construct() {
        parent::__construct();
        $this->load->database();
    }

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////   
// Esta funci�n inserta un elemento en la tabla.
    public function set_testnames($data) {
        $this->name = $data['d_name'];

        $id = $this->db->insert('tbltestnames', $this);
        return $id;
    }

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////// 
// Esta funci�n actualiza un elemento en la tabla.    
    function update_testnames($data) {
        $this->recn = $data['d_recn'];
        $this->name = $data['d_name'];
        
        $this->db->where('recn', $data['d_recn']);
        $query = $this->db->update('tbltestnames', $this);
        return $query;
    }

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////  
// Esta funci�n elimina un elemento en la tabla.
    public function del_testnames($recn) {
        $query = $this->db->delete('tbltestnames', array('recn' => $recn));
        return $query;
    }

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////   
// Esta funci�n busca elementos en la tabla por nombre
    public function find_testnames($data) {
        $this->db->select('tbltestnames.*');
        $this->db->from('tbltestnames');
        $this->db->where('recn', $data['d_recn']);
        $query = $this->db->get();
        return $query->result_array();
    }

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// Retorna todos los datos de un elemento solicitado.
    public function get_one_testnames($recn) {
        $this->db->select('tbltestnames.*');
        $this->db->from('tbltestnames');
        $this->db->where('recn', $recn);
        $query = $this->db->get();
        return $query->result_array();
    }

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// Retorna todos los datos de todos los elementos.
    public function get_all_testnames() {
        $query = $this->db->get('tbltestnames');
        return $query->result_array();
    }

