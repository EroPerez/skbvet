<?php

class M_tblunits extends CI_Model {

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////// 
//Tabla tblCountries Atributos 
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

    var $recn = '';
    var $name = '';
    var $type = '';

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////     
// M�todos de la clase tblCountries .
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//Construtor de la clase.

    public function __construct() {
        parent::__construct();
        $this->load->database();
    }

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////   
/// Esta funci�n inserta un elemento en la tabla.

    public function set_Units($data) {
        $this->name = $data['d_name'];
        //$this->type = $data['d_type'];  
        $query = $this->db->insert('tblunits', $this);
        return $query;
    }

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////// 
// Esta funci�n actualiza un elemento en la tabla.

    function update_Units($data) {
        $this->recn = $data['d_recn'];
        $this->name = $data['d_name'];
        //$this->type = $data['d_type'];        
        $this->db->where('recn', $data['d_recn']);
        $query = $this->db->update('tblunits', $this);
        return $query;
    }

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////   
// Esta funci�n elimina un elemento en la tabla.

    public function del_Units($recn) {
        $query = $this->db->delete('tblunits', array('recn' => $recn));
        return $query;
    }

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////   
// Esta funci�n busca elementos en la tabla por nombre

    public function find_Units($data) {
        $this->db->select('tblunits.*');
        $this->db->from('tblunits');
        $this->db->like('name', $data['d_name']);
        $query = $this->db->get();
        return $query->result_array();
    }

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/// Retorna todos los datos de un elemento solicitado.

    public function get_one_Units($recn) {
        $query = $this->db->get_where('tblunits', array('recn' => $recn));
        return $query->result_array();
    }

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/// Retorna todos los datos de todos los elementos.

    public function get_all_Units() {
        $query = $this->db->get('tblunits');
        return $query->result_array();
    }

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/// Retorna todos los datos de un elemento solicitado.

    public function get_units_by_type($type) {
        $query = $this->db->get_where('tblunits', array('type' => $type));
        return $query->result_array();
    }

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
}

