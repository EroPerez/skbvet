<?php
class M_tblowners extends CI_Model 
{
		
/////////////////////////////////////////////////////////////////////////////////////////////////////////////// 
//Tabla tblOwners Atributos 
///////////////////////////////////////////////////////////////////////////////////////////////////////////////

		var $recn = '';
		var $name = '';   
			
///////////////////////////////////////////////////////////////////////////////////////////////////////////////     
// Métodos de la clase tblOwners.
///////////////////////////////////////////////////////////////////////////////////////////////////////////////

///////////////////////////////////////////////////////////////////////////////////////////////////////////////
//Construtor de la clase.

public function __construct()
	{
	 parent::__construct();
	 $this->load->database(); 
	}
///////////////////////////////////////////////////////////////////////////////////////////////////////////////

///////////////////////////////////////////////////////////////////////////////////////////////////////////////   
/// Esta función inserta un elemento en la tabla.

public function set_Owners($data)
    {          
        $this->name = $data['d_name'];          
        $query = $this->db->insert('tblowners',$this); 
		return $query;
    }

//////////////////////////////////////////////////////////////////////////////////////////////////////////////

////////////////////////////////////////////////////////////////////////////////////////////////////////////// 
// Esta función actualiza un elemento en la tabla.
    
function update_Owners($data)
    { 
        $this->recn = $data['d_recn']; 
        $this->name = $data['d_name'];         
        $this->db->where('recn', $data['d_recn']);
        $query = $this->db->update('tblowners',$this);
		return $query;
    }
    
//////////////////////////////////////////////////////////////////////////////////////////////////////////////

//////////////////////////////////////////////////////////////////////////////////////////////////////////////   
// Esta función elimina un elemento en la tabla.

public function del_Owners($recn)
    {
     $query = $this->db->delete('tblowners', array('recn' => $recn));
	 return $query;
    }
/////////////////////////////////////////////////////////////////////////////////////

//////////////////////////////////////////////////////////////////////////////////////////   
// Esta función busca elementos en la tabla por nombre

public function find_Owners($data)
    {
     $this->db->select('tblowners.*');
     $this->db->from('tblowners');
     $this->db->like('name', $data['d_name']);   
     $query = $this->db->get();    
     return $query->result_array();     
    }

/////////////////////////////////////////////////////////////////////////////////////
/// Retorna todos los datos de un elemento solicitado.

public function get_one_Owners($recn)
	{
		$query = $this->db->get_where('tblowners', array('recn' => $recn));
		return $query->result_array();     
	}                                     

/////////////////////////////////////////////////////////////////////////////////////
/// Retorna todos los datos de todos los elementos.

public function get_all_Owners()
	{
		$query = $this->db->get('tblowners');
		return $query->result_array();     
	}

}   
    
