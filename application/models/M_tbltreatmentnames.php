<?php

class M_tbltreatmentnames extends CI_Model {

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//Tabla tblCountries Atributos 
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////

    var $recn = '';
    var $name = '';
    var $default = '';

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////     
// M�todos de la clase tblCountries .
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//Construtor de la clase.

    public function __construct() {
        parent::__construct();
        $this->load->database();
    }

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////   
/// Esta funci�n inserta un elemento en la tabla.

    public function set_TreatmentNames($data) {
        $this->name = $data['d_name'];
        $this->default = $data['d_default'];
        $query = $this->db->insert('tbltreatmentnames', $this);
        return $query;
    }

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////// 
// Esta funci�n actualiza un elemento en la tabla.

    function update_TreatmentNames($data) {
        $this->recn = $data['d_recn'];
        $this->name = $data['d_name'];
        $this->default = $data['d_default'];
        $this->db->where('recn', $data['d_recn']);
        $query = $this->db->update('tbltreatmentnames', $this);
        return $query;
    }

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////   
// Esta funci�n elimina un elemento en la tabla.

    public function del_TreatmentNames($recn) {
        $query = $this->db->delete('tbltreatmentnames', array('recn' => $recn));
        return $query;
    }

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////   
// Esta funci�n busca elementos en la tabla por nombre

    public function find_TreatmentNames($data) {
        $this->db->select('tbltreatmentnames.*');
        $this->db->from('tbltreatmentnames');
        $this->db->like('name', $data['d_name']);
        $query = $this->db->get();
        return $query->result_array();
    }

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/// Retorna todos los datos de un elemento solicitado.

    public function get_one_TreatmentNames($recn) {
        $query = $this->db->get_where('tbltreatmentnames', array('recn' => $recn));
        return $query->result_array();
    }

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/// Retorna todos los datos de todos los elementos.

    public function get_all_TreatmentNames() {
        $query = $this->db->get('tbltreatmentnames');
        return $query->result_array();
    }

}

