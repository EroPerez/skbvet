<?php
class M_tblspecies extends CI_Model 
{
		
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////// 
//Tabla tblCountries Atributos 
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

		var $recn = '';
		var $name = '';   
			
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////     
// Métodos de la clase tblCountries .
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//Construtor de la clase.

public function __construct()
	{
	 parent::__construct();
	 $this->load->database(); 
	}
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////   
/// Esta función inserta un elemento en la tabla.

public function set_Species($data)
    {      
        $this->name = $data['d_name'];    
        $query = $this->db->insert('tblspecies',$this); 
		return $query;
    }

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////// 
// Esta función actualiza un elemento en la tabla.
    
function update_Species($data)
    { 
        $this->recn = $data['d_recn']; 
		$this->name = $data['d_name'];       
        $this->db->where('recn', $data['d_recn']);
        $query = $this->db->update('tblspecies',$this);
		return $query;
    }
    
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////   
// Esta función elimina un elemento en la tabla.

public function del_Species($recn)
    {
     $query = $this->db->delete('tblspecies', array('recn' => $recn));
	 return $query;
    }
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////   
// Esta función busca elementos en la tabla por nombre

public function find_Species($data)
    {
     $this->db->select('tblspecies.*');
     $this->db->from('tblspecies');
     $this->db->like('name', $data['d_name']);   
     $query = $this->db->get();    
     return $query->result_array();     
    }

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/// Retorna todos los datos de un elemento solicitado.

public function get_one_Species($recn)
	{
		$query = $this->db->get_where('tblspecies', array('recn' => $recn));
		return $query->result_array();     
	}                                     

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/// Retorna todos los datos de todos los elementos.

public function get_all_Species()
	{
		$query = $this->db->get('tblspecies');
		return $query->result_array();     
	}

}   
    
