<?php

class M_tbldistricts extends CI_Model {

/////////////////////////////////////////////////////////////////////////////////////////////////////////////
//Tabla tblDistricts Atributos 
/////////////////////////////////////////////////////////////////////////////////////////////////////////////

    var $recn = '';
    var $name = '';
    var $region = '';

/////////////////////////////////////////////////////////////////////////////////////////////////////////////    
// M�todos de la clase tblDistricts.
/////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////
//Construtor de la clase.

    public function __construct() {
        parent::__construct();
        $this->load->database();
    }

/////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////  
/// Esta funci�n inserta un elemento en la tabla.

    public function set_Districts($data) {
        $this->name = $data['d_name'];
        $this->region = $data['d_region'];
        $query = $this->db->insert('tbldistricts', $this);
        return $query;
    }

/////////////////////////////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////////////////////// 
// Esta funci�n actualiza un elemento en la tabla.

    function update_Districts($data) {
        $this->recn = $data['d_recn'];
        $this->name = $data['d_name'];
         $this->region = $data['d_region'];
        $this->db->where('recn', $data['d_recn']);
        $query = $this->db->update('tbldistricts', $this);
        return $query;
    }

////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////   
// Esta funci�n elimina un elemento en la tabla.

    public function del_Districts($recn) {
        $query = $this->db->delete('tbldistricts', array('recn' => $recn));
        return $query;
    }

////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////   
// Esta funci�n busca elementos en la tabla por nombre

    public function find_Districts($data) {
        $this->db->select('tbldistricts.*');
        $this->db->from('tbldistricts');
        $this->db->like('name', $data['d_name']);
        $this->db->or_like('region', $data['d_region']);
        $query = $this->db->get();
        return $query->result_array();
    }

////////////////////////////////////////////////////////////////////////////////////////////////////////////
/// Retorna todos los datos de un elemento solicitado.

    public function get_one_Districts($recn) {
        $query = $this->db->get_where('tbldistricts', array('recn' => $recn));
        return $query->result_array();
    }

////////////////////////////////////////////////////////////////////////////////////////////////////////////
/// Retorna todos los datos de todos los elementos.

    public function get_all_Districts() {
        $query = $this->db->get('tbldistricts');
        return $query->result_array();
    }

}

